	<h2>Wp Articles Setting</h2>
</div>
<p>This plugin is the most effective plugin which easily display your posts and pages and category and also sitemap through shortcode on front end.</p>
<p>For Pages Please use this shortcode [pages], if you want to exclude some pages than use [pages excludepage="1"]</p>
<p>For Posts Please use this shortcode [posts], if you want to exclude some posts than use [posts excludepost="1"]</p>
<p>For Category Please use this shortcode [cat], if you want to exclude some category than use [cat excludecat="1"]</p>
<p>If you want to show the sitemap of site so please use this shortcode[sitelist], if you want to exclude some pages or post in sitemap so please use this type [sitelist excludepost="1" excludepage="1"]</p>
<p>If you want to hide the posts in sitemap please use this [sitelist showpost="no"]</p>
<p>In PHP files please use this type 'do_shortcode('[pages]');'</p>
